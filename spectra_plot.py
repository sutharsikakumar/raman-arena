# ════════════════════════════════════════════════════════════
# Helper: draw a styled panel on a blank axes
# ════════════════════════════════════════════════════════════
def draw_panel(ax_p, title, header_color, bg_color, border_color,
               lines, font_size=8.0):
    """
    Draws a clean rounded box filling the axes area.
    Lines that exceed available vertical space get a trailing '...'
    """
    ax_p.set_facecolor('white')
    ax_p.set_xticks([])
    ax_p.set_yticks([])
    for sp in ax_p.spines.values():
        sp.set_visible(False)

    ax_p.set_title(title, fontsize=11, color='#333333', pad=8)

    # Outer rounded rect (fills almost the whole axes)
    outer = FancyBboxPatch(
        (0.02, 0.02), 0.96, 0.96,
        boxstyle='round,pad=0.02',
        linewidth=1.4,
        edgecolor=border_color,
        facecolor=bg_color,
        transform=ax_p.transAxes,
        clip_on=False, zorder=2,
    )
    ax_p.add_patch(outer)

    # Header bar
    header_h = 0.072
    header_rect = FancyBboxPatch(
        (0.02, 0.96 - header_h), 0.96, header_h,
        boxstyle='round,pad=0.01',
        linewidth=0,
        facecolor=header_color,
        alpha=0.22,
        transform=ax_p.transAxes,
        clip_on=False, zorder=3,
    )
    ax_p.add_patch(header_rect)

    ax_p.text(0.055, 0.96 - header_h / 2,
              title,
              transform=ax_p.transAxes,
              va='center', ha='left',
              fontsize=10, color=border_color,
              fontfamily='DejaVu Sans Mono',
              fontweight='bold',
              zorder=4)

    # Body text — estimate lines that fit
    fig_h_in = ax_p.get_figure().get_size_inches()[1]
    ax_h_frac = 0.96 - header_h - 0.04   # usable fraction below header
    # rough px/line ratio
    line_frac = font_size * 1.55 / (fig_h_in * 72)
    max_lines  = int(ax_h_frac / line_frac)

    display_lines = lines[:max_lines]
    if len(lines) > max_lines:
        display_lines[-1] = '...'

    body = '\n'.join(display_lines)
    ax_p.text(0.045, 0.96 - header_h - 0.025,
              body,
              transform=ax_p.transAxes,
              va='top', ha='left',
              fontsize=font_size,
              color='#2a2a2a',
              fontfamily='DejaVu Sans Mono',
              linespacing=1.5,
              zorder=4)


